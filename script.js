let koreanVoice = null;
let englishVoice = null;

function loadVoices() {
  const voices = window.speechSynthesis.getVoices();
  koreanVoice = voices.find(v => v.lang.includes('ko') && (v.name.includes('Natural') || v.name.includes('Google')))
             || voices.find(v => v.lang.includes('ko'));
  englishVoice = voices.find(v => v.lang.includes('en') && (v.name.includes('Natural') || v.name.includes('Google')))
              || voices.find(v => v.lang.includes('en'));
}

if ('speechSynthesis' in window) {
  window.speechSynthesis.onvoiceschanged = loadVoices;
  loadVoices();
}

function safeCancelSpeech() {
  try {
    if ('speechSynthesis' in window) {
      window.speechSynthesis.cancel();
    }
  } catch (e) {
    console.warn("Speech cancel failed:", e);
  }
}

function playSound(text) {
  if (!text) return;
  if ('speechSynthesis' in window) {
    safeCancelSpeech(); 
    
    const utterance = new SpeechSynthesisUtterance(text);
    
    const hasEnglish = /[a-zA-Z]/.test(text) && !text.includes("맞다") && !text.includes("틀리다");
    if (hasEnglish) {
      utterance.lang = 'en-US';
      if (englishVoice) utterance.voice = englishVoice;
      utterance.rate = 0.95;
    } else {
      utterance.lang = 'ko-KR';
      // If it's an OX option, user wants '기본 목소리' (default voice), so we bypass 'koreanVoice' custom selection
      if (text.includes("맞다 (O)") || text.includes("틀리다 (X)")) {
        // Leave voice as default system voice
        utterance.rate = 1.0;
      } else {
        if (koreanVoice) utterance.voice = koreanVoice;
        utterance.rate = 0.9; 
      }
    }
    
    utterance.pitch = 1.0;
    window.speechSynthesis.speak(utterance);
  }
}

