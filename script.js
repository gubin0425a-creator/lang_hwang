function startLesson(nodeIndex) {
  if ('speechSynthesis' in window) {
    safeCancelSpeech();
  }

  activeLessonIndex = nodeIndex;
  currentLessonData = generateLessonData(nodeIndex);
  currentQuestionIndex = 0;
  isAnswerChecked = false;
  selectedOptionIndex = null;
  currentLessonStreak = 0;
  lessonConsecutive10Achieved = false;

  document.getElementById('dashboard-view').style.display = 'none';
  document.getElementById('lesson-view').style.display = 'flex';

  renderQuestion();
}

function startReviewLesson() {
  if (appState.incorrectNotes.length === 0) return;

  if ('speechSynthesis' in window) {
    safeCancelSpeech();
  }

  // Shuffle and pick up to 5
  const shuffled = [...appState.incorrectNotes].sort(() => Math.random() - 0.5);
  const reviewSet = shuffled.slice(0, 5);

  currentLessonData = reviewSet.map(note => ({
    question: note.question,
    options: note.options,
    answer: note.answer,
    typeLabel: "디버그 모드",
    targetTerm: "복습", 
    concept: {
      desc: note.conceptDesc,
      mnemonic: note.conceptMnemonic
    },
    isReview: true
  }));

  activeLessonIndex = -1; // Mark as review lesson so progress isn't updated
  currentQuestionIndex = 0;
  isAnswerChecked = false;
  selectedOptionIndex = null;
  currentLessonStreak = 0;
  lessonConsecutive10Achieved = false;

  document.getElementById('dashboard-view').style.display = 'none';
  document.getElementById('lesson-view').style.display = 'flex';

  renderQuestion();
}

function renderQuestion() {
  if ('speechSynthesis' in window) {
    safeCancelSpeech();
  }
  const currentQ = currentLessonData[currentQuestionIndex];

  // Update progress bar
  const progress = (currentQuestionIndex / currentLessonData.length) * 100;
  document.getElementById('lesson-progress-fill').style.width = `${progress}%`;

  // Update Type Badge
  document.getElementById('lesson-type-badge').textContent = currentQ.typeLabel;

  // Update Question Text
  document.getElementById('question-text').textContent = currentQ.question;

  // Hide Sound Button to prevent answer leaks
  const soundBtn = document.getElementById('play-question-sound');
  if (soundBtn) {
    soundBtn.style.display = 'none';
  }

  // Render Options
  const optionsContainer = document.getElementById('options-container');
  optionsContainer.innerHTML = '';

  currentQ.options.forEach((option, index) => {
    const btn = document.createElement('button');
    btn.className = 'option-btn';
    btn.textContent = option;
    btn.addEventListener('click', () => selectOption(index));
    optionsContainer.appendChild(btn);
  });

  resetFooter();
}

function selectOption(index) {
  if (isAnswerChecked) return;

  selectedOptionIndex = index;
  
  // Play the sound of the selected option
  const currentQ = currentLessonData[currentQuestionIndex];
  playSound(currentQ.options[index]);

  const options = document.querySelectorAll('.option-btn');
  options.forEach((btn, i) => {
    if (i === index) {
      btn.classList.add('selected');
    } else {
      btn.classList.remove('selected');
    }
  });

  document.getElementById('check-btn').disabled = false;
}

// Handle Check Button Click
function handleCheckBtnClick() {
  if (!isAnswerChecked) {
    checkAnswer();
  } else {
    nextQuestion();
  }
}

function checkAnswer() {
  if (selectedOptionIndex === null) return;

  isAnswerChecked = true;
  const currentQ = currentLessonData[currentQuestionIndex];
  const isCorrect = selectedOptionIndex === currentQ.answer;

  const options = document.querySelectorAll('.option-btn');
  const footer = document.getElementById('lesson-footer');
  const checkBtn = document.getElementById('check-btn');
  const feedbackMsg = document.getElementById('feedback-message');
  const consoleCard = document.getElementById('lesson-console-card');

  if (isCorrect) {
    options[selectedOptionIndex].classList.add('correct');
    options[selectedOptionIndex].classList.remove('selected');

    if (currentQ.isReview) {
      appState.incorrectNotes = appState.incorrectNotes.filter(n => n.question !== currentQ.question);
      saveState();
    }

    footer.classList.add('correct-state');
    if (consoleCard) consoleCard.classList.add('correct-state');
    
    currentLessonStreak++;
    if (currentLessonStreak >= 10) {
      lessonConsecutive10Achieved = true;
    }
    let streakRecovered = false;
    if (currentLessonStreak > 0 && currentLessonStreak % 5 === 0) {
      if (appState.hearts < 5) {
        appState.hearts = Math.min(5, appState.hearts + 1);
        streakRecovered = true;
        renderStats();
        saveState();
      }
    }

    if (streakRecovered) {
      feedbackMsg.innerHTML = `<i data-lucide="zap" size="32" fill="currentColor" style="color: var(--color-accent-mint);"></i> 정답! ${currentLessonStreak}연속 매칭으로 싱크 배터리 1개 복구! ⚡`;
      playSound("배터리가 충전되었습니다");
    } else {
      feedbackMsg.innerHTML = `<i data-lucide="check-circle" size="32"></i> 싱크 매칭 성공! (${currentLessonStreak}연속 매칭 🔥)`;
      playSound("정답");
    }
    feedbackMsg.className = 'feedback-message correct-text';
  } else {
    options[selectedOptionIndex].classList.add('wrong');
    options[selectedOptionIndex].classList.remove('selected');

    // Highlight correct answer
    options[currentQ.answer].classList.add('correct');

    footer.classList.add('wrong-state');
    if (consoleCard) consoleCard.classList.add('wrong-state');
    
        // Save to incorrect notes
    if (currentQ.concept) {
      appState.incorrectNotes.push({
        question: currentQ.question,
        options: currentQ.options,
        answer: currentQ.answer,
        unit: appState.selectedUnit,
        conceptDesc: currentQ.concept.definition || currentQ.concept.desc || "",
        conceptMnemonic: currentQ.concept.mnemonic,
        timestamp: Date.now()
      });
    }

    // Fail Logic: Push to end or force skip
    currentQ.failCount = (currentQ.failCount || 0) + 1;
    if (currentQ.failCount < 10) {
      currentLessonData.push(Object.assign({}, currentQ, { failCount: currentQ.failCount }));
    }

    // Show mnemonic feedback
    let feedbackHTML = `<div style="display:flex; flex-direction:column; gap:8px;">
      <div style="display:flex; align-items:center; gap:8px; font-size:24px; font-weight:800;">
        <i data-lucide="x-circle" size="32"></i> 싱크 에러 발생.
      </div>`;
    if (currentQ.concept) {
      feedbackHTML += `
      <div style="background:rgba(255,255,255,0.2); padding:12px; border-radius:12px; font-size:16px; line-height:1.4;">
        <strong>💡 핵심 요약:</strong> ${currentQ.concept.definition || currentQ.concept.desc}<br/>
        <strong>🧠 연상 암기:</strong> ${currentQ.concept.mnemonic}
      </div>`;
    }
    feedbackHTML += `</div>`;

    feedbackMsg.innerHTML = feedbackHTML;
    feedbackMsg.className = 'feedback-message wrong-text';
    checkBtn.classList.add('wrong-btn');

    currentLessonStreak = 0;
    appState.hearts = Math.max(0, appState.hearts - 1);
    renderStats();
    saveState();
    
      playSound("오답");
  }

  checkBtn.textContent = '계속하기';
  lucide.createIcons();
}

function nextQuestion() {
  currentQuestionIndex++;



  if (currentQuestionIndex >= currentLessonData.length) {
    finishLesson();
  } else {
    isAnswerChecked = false;
    selectedOptionIndex = null;
    renderQuestion();
  }
}

